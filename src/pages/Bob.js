const formGR = (gr) => {

    const charGR = [];

    if (gr.pre.length !== 0) {
        charGR.push(<span className="pre">{gr.pre}</span>);
    }
    if (gr.clr.length !== 0) {
        charGR.push(<span className={"clr clr__tone" + gr.tone}>{gr.clr}</span>);
    }
    if (gr.post.length !== 0) {
        charGR.push(<span className="post">{gr.post}</span>);
    }
    return charGR;
}



